public class Datatype{
	public static void main(String[] args) {
		String a = "12345";
		System.out.print(a);  // 개행문자 포함X 이어서 출력됨
		System.out.println(a);  // 개행문자 포함됨
		System.out.println("-------");  // sysout + ctrl + space 자동완성기능
		System.out.println(6);  // Number
		System.out.println("six");  // String
		// 기본 연산 구조는 파이썬과 동일함
		System.out.println(6 + 6);  // 12
		System.out.println("6" + "6");  // 66
		System.out.println("1111".length());  // len 기능
		
	}
}