
public class method2why3 {

	public static void main(String[] args) {
		
		// 메소드를 이용하여 코드를 재사용하는 방법
		printTwoTimesA();		
		printTwoTimesB();

	}

	public static void printTwoTimesB() {
		System.out.println("-");
		System.out.println("b");
		System.out.println("b");
	}

	public static void printTwoTimesA() {
		System.out.println("-");
		System.out.println("a");
		System.out.println("a");
	}
	

}
